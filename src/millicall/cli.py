# Management commands
# ---------------------------------------------------------------------------


def cmd_start() -> None:
    compose = _find_compose()
    if not compose:
        print("Error: docker compose not found.")
        sys.exit(1)

    # Detect if tunnel is configured
    env_path = PROJECT_ROOT / ".env"
    use_tunnel = False
    if env_path.exists():
        content = env_path.read_text()
        for line in content.splitlines():
            if line.startswith("CLOUDFLARE_TUNNEL_TOKEN=") and line.split("=", 1)[1].strip():
                use_tunnel = True
                break

    cmd = [*compose, "up", "-d", "--build"]
    if use_tunnel:
        cmd = [*compose, "--profile", "tunnel", "up", "-d", "--build"]
        print("Starting Millicall PBX (with Cloudflare Tunnel)...")
    else:
        print("Starting Millicall PBX...")

    result = _run(cmd)
    if result.returncode == 0:
        print("")
        print("  Web UI: http://localhost")
        if not env_path.exists():
            print("")
            print("  .env がないため自動生成されたシークレットを使用中。")
            print("  パスワード確認: docker compose logs millicall 2>&1 | grep ADMIN_PASSWORD")
    sys.exit(result.returncode)


def cmd_stop() -> None:
    compose = _find_compose()
    if not compose:
        print("Error: docker compose not found.")
        sys.exit(1)
    _run([*compose, "--profile", "tunnel", "down"])


def cmd_logs() -> None:
    compose = _find_compose()
    if not compose:
        print("Error: docker compose not found.")
        sys.exit(1)
    with contextlib.suppress(KeyboardInterrupt):
        _run([*compose, "logs", "-f", "--tail", "100"])


def cmd_status() -> None:
    compose = _find_compose()
    if not compose:
        print("Error: docker compose not found.")
        sys.exit(1)
    _run([*compose, "ps", "-a"])


async def generate_config() -> None:
    """Generate Asterisk config files from database."""
    from millicall.infrastructure.asterisk.config_writer import AsteriskConfigWriter
    from millicall.infrastructure.database import async_session, engine
    from millicall.infrastructure.orm import metadata
    from millicall.infrastructure.repositories.extension_repo import ExtensionRepository
    from millicall.infrastructure.repositories.peer_repo import PeerRepository
    from millicall.infrastructure.repositories.trunk_repo import TrunkRepository

    async with engine.begin() as conn:
        await conn.run_sync(metadata.create_all)

    async with async_session() as session:
        ext_repo = ExtensionRepository(session)
        peer_repo = PeerRepository(session)
        trunk_repo = TrunkRepository(session)

        extensions = await ext_repo.get_all()
        peers = await peer_repo.get_all()
        trunks = await trunk_repo.get_all_enabled()

    peer_map = {p.id: p for p in peers if p.id is not None}
    writer = AsteriskConfigWriter()
    writer.write_pjsip_config(peers, trunks=trunks)
    writer.write_extensions_config(extensions, peer_map, trunks=trunks)
    writer.write_ari_config()
    logger.info("Asterisk config files generated successfully")


