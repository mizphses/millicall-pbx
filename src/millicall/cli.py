USAGE = """\
Millicall PBX

Usage: millicall <command>

Commands:
  setup             Interactive setup wizard
  start             Build and start services (auto-detects tunnel)
  stop              Stop all services
  logs              Follow service logs
  status            Show service status
  generate-config   Generate Asterisk config from DB

Quick start:
  millicall setup
"""


def main() -> None:
    command = sys.argv[1] if len(sys.argv) > 1 else "setup"

    commands: dict[str, Callable[[], None]] = {
        "setup": cmd_setup,
        "init": cmd_setup,
        "start": cmd_start,
        "stop": cmd_stop,
        "logs": cmd_logs,
        "status": cmd_status,
        "generate-config": lambda: asyncio.run(generate_config()),
        "help": lambda: print(USAGE),
        "--help": lambda: print(USAGE),
        "-h": lambda: print(USAGE),
    }

    handler = commands.get(command)
    if handler:
        handler()
    else:
        print(f"Unknown command: {command}")
        print(USAGE)
        sys.exit(1)


if __name__ == "__main__":
    main()
